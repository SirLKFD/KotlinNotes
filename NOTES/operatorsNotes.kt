/*

Although the + operator is often used to add together two values, like in the example above,
it can also be used to add together a variable and a value, or a variable and a variable:

var sum1 = 100 + 50       // 150 (100 + 50)
var sum2 = sum1 + 250     // 400 (150 + 250)
var sum3 = sum2 + sum2    // 800 (400 + 400)

Operator	        Example     	                Same As
=	                x = 5	                         x = 5
+=	                x += 3	                         x = x + 3
-=	                x -= 3	                         x = x - 3
*=	                x *= 3	                         x = x * 3
/=	                x /= 3	                         x = x / 3
%=	                x %= 3	                         x = x % 3

Kotlin Comparison Operators

Operator	        Name	                          Example
==	                Equal to	                      x == y
!=	                Not equal	                      x != y
>	                Greater than	                  x > y
<	                Less than	                      x < y
>=	                Greater than or equal to	      x >= y
<=	                Less than or equal to	          x <= y

Kotlin Logical Operators

Operator	       	Description                                                 Example
&& 	                Returns true if both statements are true	                x < 5 &&  x < 10
|| 	                Returns true if one of the statements is true	            x < 5 || x < 4
!	                Reverse the result, returns false if the result is true     x != 5


 */