// FOR LOOP IN KOTLIN

/*

FOR LOOP IS DIFFERENT IN KOTLIN FROM OTHER PROGRAMMING LANGUAGES,

Unlike C# and other programming languages, there is no traditional for loop in Kotlin.

In Kotlin, the for loop is used to loop through arrays, ranges,
and other things that contains a countable number of values.

INSTEAD IT USES RANGES...

You will learn more about ranges in the next chapter - which will create a range of values.



FOR LOOP:
Often when you work with arrays, you need to loop through all of the elements.
To loop through array elements, use the for loop together with the in operator:

val cars = arrayOf("Volvo", "BMW", "Ford", "Mazda")
for (x in cars) {
  println(x)
}

// displays Volvo BMW Ford Mazda

val nums = arrayOf(1, 5, 10, 15, 20)
for (x in nums) {
  println(x)
}

// displays 1 5 10 15 20





 */